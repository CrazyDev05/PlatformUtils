package de.crazydev22.platformutils;

/**
 * Represents different types of Minecraft server implementations.
 * <p>
 * This enum can be used to specify or identify the server type
 * within applications or utilities that interact with Minecraft servers.
 */
public enum Type {
    /**
     * A server implementation type representing Folia
     */
    FOLIA,
    /**
     * A server implementation type representing Paper
     */
    PAPER,
    /**
     * A server implementation type representing Spigot
     */
    SPIGOT
}
